package com.gaosuchuxing.form.search;

public class UserCouponSearchForm {
    private String searchKey;    
    private String keyword;
    private String userCouponStatus; 

    public String getSearchKey() {
        return searchKey;
    }

    public void setSearchKey(String searchKey) {
        this.searchKey = searchKey;
    }


    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public String getUserCouponStatus() {
        return userCouponStatus;
    }

    public void setUserCouponStatus(String userCouponStatus) {
        this.userCouponStatus = userCouponStatus;
    }

}
